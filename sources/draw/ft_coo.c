/*
** ft_coo convert the given 3D coordinate into 2D one.
*/

void	ft_coo(int *x, int *y, int z)
{
	int	iso_x;
	int	iso_y;

	iso_x = (*x - z) * 0.894427191;
	iso_y = *y + (*x + z) * 0.4472135955;
	*x = iso_x;
	*y = iso_y;
}
